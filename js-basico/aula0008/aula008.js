/**
 * Aritméticos
 * + Adição / Concatenação
 * - Subtração
 * / Divisão
 * * Multiplicação
 */

/**
 * Ordem de prescedência
 * ()
 * **
 * * / %
 * + -
 */

const num1 = 2;
const num2 = 10;
console.log(`A soma entre ${num1} + ${num2} = ${num1 + num2}`);
console.log(`A subração entre ${num1} - ${num2} = ${num1 - num2}`);
console.log(`A multiplicação entre ${num1} * ${num2} = ${num1 * num2}`);
console.log(`A divisão entre ${num1} / ${num2} = ${num1 / num2}`);

let contador = 1;
contador++; // Operador de incremento
console.log(contador); // 2
